    public void actualizar(int op) {
        CB_JD1_Armas.removeAllItems();
        switch (op) {
            case 1:
                for (int i = 0; i < listArmas.size(); i++) {
                    if (listArmas.get(i).getPais().equals("Ruso")) {
                        CB_JD1_Armas.addItem(listArmas.get(i).toString());
                    }
                }
                break;
            case 2:
                for (int i = 0; i < listArmas.size(); i++) {
                    if (listArmas.get(i).getPais().equals("Aleman")) {
                        CB_JD1_Armas.addItem(listArmas.get(i).toString());
                    }
                }
                break;
            case 3:
                for (int i = 0; i < listArmas.size(); i++) {
                    if (listArmas.get(i).getPais().equals("Alumno")) {
                        CB_JD1_Armas.addItem(listArmas.get(i).toString());
                    }
                }
                break;
        }
    }
    
