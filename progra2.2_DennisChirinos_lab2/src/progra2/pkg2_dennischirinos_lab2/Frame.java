            int armies = 3;
            if (Army1.isEmpty()) {
                armies--;
            }
            if (Army2.isEmpty()) {
                armies--;
            }
            if (Army3.isEmpty()) {
                armies--;
            }
            if (armies <= 1) {
                victoria = false;
            }
